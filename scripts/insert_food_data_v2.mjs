function toRows(raw) {
  return raw.map(([food_name, food_category, cal, prot, fat, carb, fiber, sodium, sugar]) => ({
    food_name,
    food_category,
    calories_per_g: cal    / 100,
    protein_per_g:  prot   / 100,
    fat_per_g:      fat    / 100,
    carbs_per_g:    carb   / 100,
    fiber_per_g:    fiber  / 100,
    sodium_per_g:   sodium / 100,
    sugar_per_g:    sugar  / 100,
    source: '식약처',
  }))
}

async function insertBatch(rows) {
  const res = await fetch(`${SUPABASE_URL}/rest/v1/food_master`, {
    method:  'POST',
    headers: {
      'apikey':        SERVICE_KEY,
      'Authorization': `Bearer ${SERVICE_KEY}`,
      'Content-Type':  'application/json',
      'Prefer':        'return=minimal',
    },
    body: JSON.stringify(rows),
  })
  if (!res.ok) throw new Error(`Insert 실패: ${await res.text()}`)
}

async function main() {
  const rows = toRows(RAW)
  console.log(`📦  ${rows.length}개 추가 데이터 적재 중...`)
  await insertBatch(rows)
  console.log(`✅  완료: ${rows.length}개 추가 적재됨`)
}

main().catch(e => { console.error('❌', e.message); process.exit(1) })
